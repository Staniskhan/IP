public class Student
{
    private Integer studentId;
    private String name;

    public Integer getID()
    {
        return studentId;
    }

    public void setID(Integer ID)
    {
        studentId = ID;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }
}